a = 2
b = 2

print("Arithmetic Operators")
print(a) # assign
print(b)
print("")
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

print("Logic Operators")
print(a == a and b == b)
print(a == a or b == b)
print(not a == b)
print(not a == b)

print("Relational or Comparison")
print(a > b)
print(a < b)
print(a == b)
print(a != b)
print(a >= b)
print(a <= b)


print("Assign operators")
a += b
a -= b
a *= b
a /= b

print("Membership operators")
c = [1,2,3,5]

print(1 in c)
print(4 not in c)

print("Identity Operators")

print(1 is 1)
print(2 is not 1)